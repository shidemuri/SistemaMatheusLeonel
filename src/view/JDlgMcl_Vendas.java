    public void beanView(MclVendas vendas){
        Mcl_Util.mcl_habilitar(false, jBtnIncluir);
        Mcl_Util.mcl_habilitar(true, jBtnAlterar,
        jBtnExcluir,
        jBtnCancelar);
        jTxtCodigo.setText(Integer.toString(vendas.getMclIdVendas()));
        jTxtTotal.setText(Double.toString(vendas.getMclTotal()));
        jFmtDataVenda.setText(Mcl_Util.dateToStr(vendas.getMclDataVenda()));
        jCboVendedor.setSelectedItem(vendas.getMclVendedor());
        jCboCliente.setSelectedItem(vendas.getMclClientes());
    }
    
    public MclVendas viewBean() {
        MclVendas vendas = new MclVendas();
        vendas.setMclIdVendas(Mcl_Util.strToInt(jTxtCodigo.getText()));
        vendas.setMclDataVenda(Mcl_Util.strToDate(jFmtDataVenda.getText()));
        vendas.setMclVendedor((MclVendedor) jCboVendedor.getSelectedItem());
        vendas.setMclTotal(Mcl_Util.strToDouble(jTxtTotal.getText()));
        vendas.setMclClientes((MclClientes) jCboCliente.getSelectedItem());
        return vendas;
    }

